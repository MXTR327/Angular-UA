let a = 3,
  b = 2;
let z = a + b;

// Incremento
// Pre-incremento (el operador ++ antes de la variable)
z = ++a;
console.log(a);

// Post-incremento (el operador ++ después de la variable)
z = b++;
console.log(b);
console.log(z);

// Decremento
// Pre-decremento (el operador -- antes de la variable)
z = --a;
console.log(a);
console.log(z);

// Post-decremento (el operador -- después de la variable)
z = b--;
console.log(b);
console.log(z);

